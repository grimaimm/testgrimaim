1. Clone Repository:
Salin repository GrimAimCode dari GitHub ke komputer Anda dengan menggunakan perintah di bawah ini di terminal atau command prompt:
bash
git clone https://github.com/username/GrimAimCode.git
2. Navigate to Project Directory: 
Buka terminal atau command prompt, lalu masuk ke direktori proyek yang baru saja Anda clone dengan perintah:
bash
cd GrimAimCode
3. Setup Virtual Environment: 
Buat lingkungan virtual Python untuk proyek ini agar dapat mengisolasi dependensinya. Gunakan perintah berikut:
- Jika Anda menggunakan venv (Python 3.3+):
python -m venv venv
- Aktifkan lingkungan virtual:
source venv/bin/activate
4. Install Dependencies: 
Instal semua dependensi yang dibutuhkan oleh proyek Flask. Pastikan Anda berada di dalam lingkungan virtual sebelum menjalankan perintah ini:
pip install -r requirements.txt